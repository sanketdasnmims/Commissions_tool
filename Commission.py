def page_review(state: dict) -> str:
    req = state.get("requirement", {})
    candidates = state.get("candidates", [])
    gaps = state.get("gaps", [])
    open_qs = state.get("open_questions", [])
    conflicts = state.get("conflicts", [])

    # Quality score indicator
    qs = req.get("quality_score", 0)
    qs_color = "emerald" if qs >= 0.7 else "amber" if qs >= 0.4 else "red"
    qs_pct = int(qs * 100)

    # Candidates summary
    cand_html = ""
    for c in candidates[:15]:
        ct = c.get("candidate_type", "other").lower()
        badge_cls = f"badge-{ct}" if ct in ["rule","actor","trigger","exception","validation"] else "badge-other"
        conf = c.get("confidence", 0)
        conf_cls = "conf-high" if conf >= 0.7 else "conf-med" if conf >= 0.4 else "conf-low"
        inferred = '<span class="text-xs text-amber-600 ml-1">⚑ inferred</span>' if c.get("is_inferred") else ""
        cand_html += f"""<div class="border-b border-slate-100 last:border-0 py-2">
          <div class="flex items-start gap-2">
            <span class="px-1.5 py-0.5 rounded text-xs font-semibold {badge_cls} whitespace-nowrap flex-shrink-0">{c.get('candidate_type','')}</span>
            <div class="flex-1">
              <p class="text-sm text-slate-700">{c.get('statement','')}{inferred}</p>
              <p class="text-xs text-slate-400 mt-0.5 italic">"{c.get('source_quote','')}"</p>
            </div>
            <span class="{conf_cls} text-xs flex-shrink-0">{int(conf*100)}%</span>
          </div>
        </div>"""
    if len(candidates) > 15:
        cand_html += f'<p class="text-xs text-slate-400 pt-2">... and {len(candidates)-15} more candidates</p>'

    # Gaps and open questions
    issues_html = ""
    if gaps:
        for g in gaps:
            sev = g.get("severity","MEDIUM")
            sev_cls = "text-red-600" if sev == "HIGH" else "text-amber-600" if sev == "MEDIUM" else "text-slate-500"
            issues_html += f'<li class="text-sm {sev_cls}"><strong>[{sev}]</strong> {g.get("description","")}</li>'
    if open_qs:
        for q in open_qs:
            issues_html += f'<li class="text-sm text-blue-600">❓ {q.get("question","")}</li>'
    if conflicts:
        for c in conflicts:
            issues_html += f'<li class="text-sm text-red-700">⚡ CONFLICT: {c.get("description","")}</li>'

    issues_section = ""
    if issues_html:
        issues_section = f"""<div class="card mt-4">
          <h3 class="text-sm font-semibold text-slate-700 mb-2">⚠ Gaps, Questions &amp; Conflicts</h3>
          <ul class="space-y-1">{issues_html}</ul>
        </div>"""

    # Requirement sections
    def section(label, content):
        if not content:
            return ""
        if isinstance(content, list):
            if not content:
                return ""
            items = "".join(f"<li class='text-sm text-slate-700'>{i if isinstance(i,str) else json.dumps(i)}</li>" for i in content)
            return f'<div class="mb-3"><div class="text-xs font-semibold text-slate-500 uppercase tracking-wide mb-1">{label}</div><ul class="list-disc list-inside space-y-0.5">{items}</ul></div>'
        return f'<div class="mb-3"><div class="text-xs font-semibold text-slate-500 uppercase tracking-wide mb-1">{label}</div><p class="text-sm text-slate-700">{content}</p></div>'

    def flow_section(label, steps):
        if not steps:
            return ""
        rows = ""
        for s in steps:
            if isinstance(s, dict):
                rows += f"""<tr class="border-b border-slate-100 last:border-0">
                  <td class="py-1.5 pr-3 text-xs text-slate-400 font-mono">{s.get('step','')}</td>
                  <td class="py-1.5 pr-3 text-xs text-indigo-700 font-medium">{s.get('actor','')}</td>
                  <td class="py-1.5 pr-3 text-sm text-slate-700">{s.get('action','')}</td>
                  <td class="py-1.5 text-sm text-slate-500 italic">{s.get('system_response','')}</td>
                </tr>"""
        return f"""<div class="mb-3">
          <div class="text-xs font-semibold text-slate-500 uppercase tracking-wide mb-1">{label}</div>
          <table class="w-full text-left"><tbody>{rows}</tbody></table>
        </div>"""

    def ac_section(criteria):
        if not criteria:
            return ""
        rows = ""
        for ac in criteria:
            if isinstance(ac, dict):
                testable = ac.get("testable", True)
                flag = "" if testable else ' <span class="text-red-500 text-xs font-semibold">[NOT TESTABLE]</span>'
                rows += f"""<div class="flex items-start gap-2 py-1.5 border-b border-slate-100 last:border-0">
                  <span class="text-xs font-mono text-indigo-600 flex-shrink-0 pt-0.5">{ac.get('id','')}</span>
                  <p class="text-sm text-slate-700">{ac.get('text','')}{flag}</p>
                </div>"""
