@app.route("/extract", methods=["POST"])
def extract():
    sid = session.setdefault("sid", str(uuid.uuid4()))
    state = get_state(sid)
    state["error"] = None

    if not state["sources"]:
        state["error"] = "Upload at least one source before extracting."
        return redirect(url_for("index"))

    # Combine all source text
    combined = "\n\n===== SOURCE SEPARATOR =====\n\n".join(
        f"[{s['name']}]\n{s['content']}" for s in state["sources"]
    )
    component = state["component"]

    try:
        extraction = ai_extract(combined, component)
        state["candidates"]     = extraction.get("candidates", [])
        state["gaps"]           = extraction.get("gaps", [])
        state["open_questions"] = extraction.get("open_questions", [])
        state["conflicts"]      = extraction.get("conflicts", [])

        req = ai_compose(
            state["candidates"],
            state["gaps"],
            state["open_questions"],
            component,
        )
        req["_status"] = "DRAFT"
        state["requirement"] = req
        state["stage"] = "review"
    except Exception as e:
        state["error"] = f"AI extraction failed: {e}"
        return redirect(url_for("index"))

    return redirect(url_for("review"))


